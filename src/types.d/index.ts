type Iso3166 = (typeof iso3166Codes)[number];

type BaseSkill = {
  id: UUID;
  text: string;
};

export type HardSkill = BaseSkill & {
  kind: "hard";
};

export type SoftSkill = BaseSkill & {
  kind: "soft";
};

export type LanguageSkill = BaseSkill & {
  kind: "language";
  code: Iso639;
  flag: Iso3166;
};

export type Skill = HardSkill | SoftSkill | LanguageSkill;

export type SkillKind = Skill["kind"];

